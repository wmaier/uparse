package de.hhu.phil.uparse.eval.constituent;

/**
 * A single bracket in a treebank consisting of label and two indices.
 * @author wmaier
 *
 */
public class Bracket {
	
	public int i;
	
	public int j;
	
	public String label;
	
	public boolean seen;
	
	public Bracket(int i, int j, String label) {
		this.i = i;
		this.j = j;
		this.label = label;
	}
	
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + i;
		result = prime * result + j;
		result = prime * result + ((label == null) ? 0 : label.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Bracket other = (Bracket) obj;
		if (i != other.i)
			return false;
		if (j != other.j)
			return false;
		if (label == null) {
			if (other.label != null)
				return false;
		} else if (!label.equals(other.label))
			return false;
		return true;
	}

	public String toString() {
		return label + ":[" + i + "," + j + "]";
	}
	
}
