package de.hhu.phil.uparse.eval.constituent;

import java.util.BitSet;

/*
 * A discontinuous bracket, consisting of a label and a Bitset denoting the
 * terminals dominated by the corresponding node.
 */
public class DiscoBracket {
	
	public BitSet cover;
	
	public String label;
	
	public boolean seen;
	
	public DiscoBracket(String label, BitSet cover) {
		this.label = label;
		this.cover = cover;
	}
	
	public String toString() {
		return label + "_" + cover;
	}
	
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((cover == null) ? 0 : cover.hashCode());
		result = prime * result + ((label == null) ? 0 : label.hashCode());
		return result;
	}
	
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		DiscoBracket other = (DiscoBracket) obj;
		if (cover == null) {
			if (other.cover != null)
				return false;
		} else if (!cover.equals(other.cover))
			return false;
		if (label == null) {
			if (other.label != null)
				return false;
		} else if (!label.equals(other.label))
			return false;
		return true;
	}
	
}